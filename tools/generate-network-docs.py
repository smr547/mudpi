def validate_registry(registry: Dict[str, Any]) -> List[str]:
    errors: List[str] = []

    site_names = set(registry.get("sites", {}).keys())
    seen_fqdns: set[str] = set()
    seen_ips: Dict[str, str] = {}

    for host in registry.get("hosts", []):
        site = host.get("site")
        if site not in site_names:
            errors.append(f"Host {host.get('name')} refers to unknown site {site!r}")

        for name, ip in host_records(host, registry):
            if name in seen_fqdns:
                errors.append(f"Duplicate FQDN detected: {name}")
            else:
                seen_fqdns.add(name)

            if ip in seen_ips:
                errors.append(f"IP address {ip} reused by {name} and {seen_ips[ip]}")
            else:
                seen_ips[ip] = name

            try:
                ipaddress.ip_address(ip)
            except ValueError:
                errors.append(f"Invalid IP address for {name}: {ip}")

    return errors


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description="Generate network docs and dnsmasq artifacts from YAML registry")
    parser.add_argument(
        "--registry",
        default="docs/reference/network-registry.yaml",
        help="Path to YAML registry (default: docs/reference/network-registry.yaml)",
    )
    parser.add_argument(
        "--output",
        default="generated",
        help="Output directory (default: generated)",
    )
    parser.add_argument(
        "--fail-on-validation",
        action="store_true",
        help="Exit non-zero if registry validation reports problems",
    )
    return parser.parse_args()


def main() -> int:
    args = parse_args()
    registry_path = Path(args.registry)
    output_root = Path(args.output)

    registry = load_registry(registry_path)
    errors = validate_registry(registry)

    ensure_dir(output_root)
    ensure_dir(output_root / "dnsmasq")
    ensure_dir(output_root / "dhcp")

    generate_host_registry_md(registry, output_root / "host-registry.md")
    generate_site_summary_md(registry, output_root / "site-summary.md")
    generate_addressing_plan_md(registry, output_root / "addressing-plan.md")
    generate_network_inventory_md(registry, output_root / "network-inventory.md")

    generate_dnsmasq_hosts(registry, output_root / "dnsmasq" / "hosts.conf")
    generate_dnsmasq_ptr(registry, output_root / "dnsmasq" / "reverse-ptr.conf")
    generate_forwarding_conf(registry, "mudpi", output_root / "dnsmasq" / "forwarding-mudpi.conf")
    generate_forwarding_conf(registry, "shedpi", output_root / "dnsmasq" / "forwarding-shedpi.conf")

    for site_name in registry.get("sites", {}).keys():
        if site_name == "vpn":
            continue
        generate_site_dhcp_reservations(
            registry,
            site_name,
            output_root / "dhcp" / f"{site_name}-reservations.conf",
        )

    validation_md = [
        "# Registry Validation",
        "",
        f"Source registry: `{registry_path}`",
        "",
    ]
    if errors:
        validation_md.append("## Problems found")
        validation_md.append("")
        for err in errors:
            validation_md.append(f"- {err}")
    else:
        validation_md.append("No validation problems found.")
    write_text(output_root / "validation.md", "\n".join(validation_md))

    if errors:
        print("\nValidation reported issues:")
        for err in errors:
            print(f"  - {err}")
        if args.fail_on_validation:
            return 1

    return 0


if __name__ == "__main__":
    raise SystemExit(main())
